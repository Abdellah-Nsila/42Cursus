#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	src_len;
	size_t	dst_len;

	i = 0;	
	src_len = strlen(src);

	if (dstsize == 0)
		return (src_len);
	dst_len = ft_strlen(dst);

	if (dst_len >= dstsize)
		return (dstsize + src_len);
		
	while((dst_len + i) < dstsize - 1 && src[i])
	{
		dst[dst_len + i] = src[i];
		i++;
	}
	dst[dst_len + i] = '\0';
	return (src_len + dst_len);
}
